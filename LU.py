def yes_no(question):
    yes_no.answer = None
    clear(yes_no.answer)
    while True:
        yes_no.answer = input(question).lower().strip()
        if yes_no.answer in yes:
            yes_no.answer = True
            break

        elif yes_no.answer in no:
            yes_no.answer = False
            break

        else:
            print("Please answer with a yes or no")

